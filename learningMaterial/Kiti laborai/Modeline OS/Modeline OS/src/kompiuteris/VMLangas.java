    private char[] getIncremetedIC() {
        int ip = Integer.parseInt(new String(RealiMasina.cpu.IC));
        ip++;
        ip = ip % 100;
        String tmp = ip + "";
        if (tmp.length() < 2) {
            tmp = "0" + tmp;
        }
        return tmp.toCharArray();
    }

    private char[] getDecrementedIC() {
        int ip = Integer.parseInt(new String(RealiMasina.cpu.IC));
        ip--;
        ip = ip % 100;
        String tmp = ip + "";
        if (tmp.length() < 2) {
            tmp = "0" + tmp;
        }
        return tmp.toCharArray();
    }
    
