    public void refresh(boolean active) {
		if (!active) {
			textBoxActive.setText("N");
			return;
		}
		else 
			textBoxActive.setText("T");
        String cmd1in = "-";
		if (getWord(getDecrementedIC(), RealiMasina.cpu.PTR) != null)
			cmd1in = new String(getWord(getDecrementedIC(), RealiMasina.cpu.PTR)).toUpperCase();
        String cmd2in = "-";
		if (getWord(RealiMasina.cpu.IC, RealiMasina.cpu.PTR) != null)
			cmd2in = new String(getWord(RealiMasina.cpu.IC, RealiMasina.cpu.PTR)).toUpperCase();
        String cmd3in = "-";
		if (getWord(getIncremetedIC(), RealiMasina.cpu.PTR) != null)
			cmd3in = new String(getWord(getIncremetedIC(), RealiMasina.cpu.PTR)).toUpperCase();
        Procesorius cpu = RealiMasina.cpu;
        int pid = RealiMasina.kernel.getActiveProc().getPId();
        Procesas proc = RealiMasina.kernel.getProcess(pid);
        if (proc != null && proc.getOwnedResourceElements() != null) {
            int count = 0;
            for (Elementas ele : proc.getOwnedResourceElements())
                if (ele instanceof VartotojoAtmintiesElementas) {
                    int nr = ((VartotojoAtmintiesElementas)ele).getTakelioNr();
                    if (nr != Integer.parseInt(new String(RealiMasina.cpu.PTR))) {
						String lines[] = new String[10];
						String line = RealiMasina.memory.readTrack(nr);
						for (int i = 0; i < 10; i++)
							lines[i] = line.substring(i*4, i*4+4);
						line = "";
						for (int i = 0; i < 9; i++)
							line += lines[i] + "\t";
						line += lines[9];
                        memory[count++].setText(line);
					}
                }
        }
        id.setText(RealiMasina.kernel.getActiveProc().getId()+"");
        r1.setText(cpu.R[0]+"");
        r2.setText(cpu.R[1]+"");
        r3.setText(cpu.R[2]+"");
        r4.setText(cpu.R[3]+"");
        ic1.setText(cpu.IC[0]+"");
        ic2.setText(cpu.IC[1]+"");
		if (cpu.SP[0] == '7') {
			sp1.setText("8");
			sp2.setText("0");
		}
		else {
			sp1.setText(cpu.SP[0]+"");
			sp2.setText(cpu.SP[1]+"");
		}
        ptr1.setText(cpu.PTR[0]+"");
        if (cpu.PTR.length > 1)
			ptr2.setText(cpu.PTR[1]+"");
		else
			ptr2.setText("0");
		if (cpu.C == '0')
			c1.setText("N");
		else
			c1.setText("T");
        if (cmd1in != null && !cmd1in.equals(""))
            cmd1.setText(cmd1in);
        else cmd1.setText("-");
        if (cmd2in != null && !cmd2in.equals(""))
            cmd2.setText(cmd2in);
        else cmd2.setText("-");
        if (cmd3in != null && !cmd3in.equals(""))
            cmd3.setText(cmd3in);
        else cmd3.setText("-");
    }
    
